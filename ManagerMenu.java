    private void jbtnTaxRateChangeActionPerformed(java.awt.event.ActionEvent evt) {                                                  
        // TODO add your handling code here:
               
        String input = JOptionPane.showInputDialog(null, "Please enter the new tax rate as a percentage such as x.xx%: ", "", 
                JOptionPane.QUESTION_MESSAGE);
        double newtax = Double.parseDouble(input);
           
        if (newtax < 0) 
                throw new IllegalArgumentException(
               "new tax (" + newtax + ") must be a positive number.");

        String newTaxRate = Double.toString(newtax);
        String oldTaxRate = Double.toString(order.getTax_Rate());
        
        try (BufferedReader reader = new BufferedReader(new FileReader("food.txt"));
             BufferedWriter writer = new BufferedWriter(new FileWriter("food.tmp"))){
                    String line;
                    Boolean found = false;
                
                    while ((line = reader.readLine()) != null){
                      if (line.contains(oldTaxRate) && !found) {
                          line = line.replace(oldTaxRate, newTaxRate);
                          found = true;
                      }
                      writer.write(line + "\n");
                    }
                    writer.close();
                }
                catch (IOException e) {
                    e.printStackTrace();
                }
               // Rename temp file to replace original file
               File originalFile = new File ("food.txt");
               File tempFile = new File ("food.tmp");
               if (tempFile.exists()){
                   try {
              // Attempt to replace the original file with the temp file
                        Files.move(tempFile.toPath(), originalFile.toPath(), StandardCopyOption.REPLACE_EXISTING);
                        JOptionPane.showMessageDialog(this, "Tax rate successfully updated.");
                   }
                   catch (IOException e) {
                         e.printStackTrace();
                         JOptionPane.showMessageDialog(this, "Tax rate not updated.");
                   }
               }
               else
                    JOptionPane.showMessageDialog(this, "Tax rate not updated.");                  

    }                                                 

