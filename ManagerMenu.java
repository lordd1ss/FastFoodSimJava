    public String readNthWord(String filePath, int n) {
        File file = new File(filePath);
        try (Scanner scanner = new Scanner(file)) {
            int count = 1;
            while (scanner.hasNext()) {
                String word = scanner.next();
                if (count == n) {
                    return word;
                }
                count++;
            }
        } catch (FileNotFoundException e) {
            System.err.println("File not found: " + e.getMessage());
        }
        return null; // Returns null if the word does not exist
    }
    
    public void updateFoodPrice(String result, int targetWordIndex){
                       
        String input = JOptionPane.showInputDialog(null, "Please enter the new price in decimal format: ", "", 
                JOptionPane.QUESTION_MESSAGE);
        String newprice = (input);
        int count = 1;

        try (BufferedReader reader = new BufferedReader(new FileReader("food.txt"));
             BufferedWriter writer = new BufferedWriter(new FileWriter("food.tmp"))){
             String line;
             Boolean found = false;
            while ((line = reader.readLine()) != null){
                if (line.contains(result) && (count == targetWordIndex)) {
                    line = line.replace(result, newprice);
                }
                writer.write(line + "\n");
                count++;
            }
            writer.close();
            }
            catch (IOException e) {
                e.printStackTrace();
            }
            // Rename temp file to replace original file
            File originalFile = new File ("food.txt");
            File tempFile = new File ("food.tmp");
            if (tempFile.exists()){
                try {
            // Attempt to replace the original file with the temp file
                    Files.move(tempFile.toPath(), originalFile.toPath(), StandardCopyOption.REPLACE_EXISTING);
                    JOptionPane.showMessageDialog(this, "Food item successfully updated.");
                }
                catch (IOException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Food item not updated.");
                }
            }
            else
                JOptionPane.showMessageDialog(this, "Food item not updated.");
    }
